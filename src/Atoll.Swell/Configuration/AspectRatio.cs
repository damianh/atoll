namespace Atoll.Swell.Configuration;

/// <summary>
/// Defines the standard aspect ratios for a slide deck.
/// </summary>
public enum AspectRatio
{
    /// <summary>16:9 widescreen (default). Best for modern projectors and screens.</summary>
    Ratio16x9,

    /// <summary>4:3 standard. Useful for older projectors.</summary>
    Ratio4x3,

    /// <summary>3:2 ratio.</summary>
    Ratio3x2,
}
